package main

import "fmt"

func main() {
	//Variable Declaration using Short Declaration Operator
	x := 42
	y := "James Bond"
	z := true

	// Single Print Statement. You can use any other method to Print it in single statement.
	fmt.Println(x, y, z)

	// Multiple Print Statements
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}
